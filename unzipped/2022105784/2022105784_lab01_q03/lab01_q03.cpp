#include <iostream>

using namespace std;
// 자료형 크기 표현하는 함수
int main() {
	cout << "(7 == 5):" << (7 == 5) << endl;
	cout << "(7 >= 5):" << (7 >= 5) << endl;
	cout << "(7 != 5):" << (7 != 5) << endl;
	cout << "(7 <= 5):" << (7 <= 5) << endl;
	cout << "(7 >= 5 ? 100 : -100): " << (7 >= 5 ? 100 : -100) << endl;
}