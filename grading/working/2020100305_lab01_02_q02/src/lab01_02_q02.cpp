#include <iostream>
using namespace std;
int main(){//sizeof()는 피연산자의 크기를 바이트 단위로 변환하는 기능을 가지고 있다.
    unsigned short siX;
    unsigned iX;
    long liX;
    long long lliX;
    cout<<"sizeof(siX):"<<sizeof(siX)<<endl;
    cout<<"sizeof(iX):"<<sizeof(iX)<<endl;
    cout<<"sizeof(liX):"<<sizeof(liX)<<endl;
    cout<<"sizeof(lliX):"<<sizeof(lliX)<<endl;
}
