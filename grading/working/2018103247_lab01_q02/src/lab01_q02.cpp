#include <iostream>

using namespace std;

// 기초문제2 //
// sizeof()의 기능 : 입력 파라메터의 byte 값을 return한다.//
int main()
{
    unsigned short siX;
    unsigned iX;
    long liX;
    long long lliX;
    cout << "sizeof(siX):" << sizeof(siX) << endl;
    cout << "sizeof(iX):" << sizeof(iX) << endl;
    cout << "sizeof(liX):" << sizeof(liX) << endl;
    cout << "sizeof(lliX):" << sizeof(lliX) << endl;
}