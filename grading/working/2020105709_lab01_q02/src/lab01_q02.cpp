//sizeof() 함수는 입력값의 크기를 byte 수로 나타내서 보여준다.

#include <iostream>

using namespace std;

int main() {
    unsigned short siX; 
    unsigned iX;
    long liX;
    long long lliX;
    cout << "sizeof(siX):" << sizeof(siX) << endl;
    cout << "sizeof(iX):" << sizeof(iX) << endl;
    cout << "sizeof(liX):" << sizeof(liX) << endl;
    cout << "sizeof(lliX):" << sizeof(lliX) << endl;

}

